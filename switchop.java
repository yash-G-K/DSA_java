import java.util.*;

public class switchop{
    public static void main(String args[]){
        int num=2;
        switch(num){
            case 1 : System.out.println("samosa");
            break;
            case 2 : System.out.println("burger");
            break;
            case 3 : System.out.println("mango");
            break;
            default : System.out.println("default");

        }
 
    }
}